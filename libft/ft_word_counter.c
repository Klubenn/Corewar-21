int	ft_word_counter(char const *s, char c)
{
	int size;
	int f;

	f = 0;
	size = 0;
	while (s[f])
	{
		if ((s[f] == c && s[f - 1] != c) || (s[f] != c && s[f + 1] == '\0'))
			size++;
		f++;
	}
	return (size);
}
